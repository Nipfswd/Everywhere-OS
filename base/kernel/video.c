static uint32_t g_ConRow = 0;

/* -----------------------------------------------------------------------
   VGA text fallback state
   -------------------------------------------------------------------- */

#define VGA_WIDTH  80
#define VGA_HEIGHT 25
#define VGA_SIZE   (VGA_WIDTH * VGA_HEIGHT * 2)

static volatile char *g_VgaBuf   = (volatile char *)0xB8000;
static int            g_VgaCursor = 0;

/* -----------------------------------------------------------------------
   VideoInitialize
   -------------------------------------------------------------------- */

VOID
VideoInitialize (
    uint64_t Address,
    uint32_t Width,
    uint32_t Height,
    uint32_t Pitch,
    uint8_t  Bpp
    )
{
    if (Address == 0 || Bpp < 24) {
        /* Stay in VGA text mode */
        g_TextMode     = 1;
        g_ScreenWidth  = 80;
        g_ScreenHeight = 25;
        return;
    }

    g_FB           = (uint8_t *)(uint32_t)Address;
    g_Pitch        = Pitch;
    g_Bpp          = (uint32_t)Bpp / 8;
    g_ScreenWidth  = Width;
    g_ScreenHeight = Height;
    g_TextMode     = 0;

    /* Paint background */
    VideoFillRect(0, 0, Width, Height, COLOR_BLACK);
}

/* -----------------------------------------------------------------------
   Pixel primitives
   -------------------------------------------------------------------- */

VOID
VideoSetPixel (
    uint32_t X,
    uint32_t Y,
    uint32_t Color
    )
{
    uint8_t *Pixel;

    if (g_TextMode || X >= g_ScreenWidth || Y >= g_ScreenHeight) {
        return;
    }

    Pixel    = g_FB + Y * g_Pitch + X * g_Bpp;
    Pixel[0] = (uint8_t)(Color & 0xFF);           /* B */
    Pixel[1] = (uint8_t)((Color >> 8)  & 0xFF);   /* G */
    Pixel[2] = (uint8_t)((Color >> 16) & 0xFF);   /* R */
    if (g_Bpp == 4) {
        Pixel[3] = 0xFF;
    }
}

VOID
VideoFillRect (
    uint32_t X,
    uint32_t Y,
    uint32_t W,
    uint32_t H,
    uint32_t Color
    )
{
    uint32_t Row, Col;

    for (Row = Y; Row < Y + H && Row < g_ScreenHeight; Row++) {
        for (Col = X; Col < X + W && Col < g_ScreenWidth; Col++) {
            VideoSetPixel(Col, Row, Color);
        }
    }
}

VOID
VideoDrawRect (
    uint32_t X,
    uint32_t Y,
    uint32_t W,
    uint32_t H,
    uint32_t Color
    )
{
    VideoFillRect(X,         Y,         W, 1, Color);
    VideoFillRect(X,         Y + H - 1, W, 1, Color);
    VideoFillRect(X,         Y,         1, H, Color);
    VideoFillRect(X + W - 1, Y,         1, H, Color);
}

VOID
VideoDrawLine (
    int X0,
    int Y0,
    int X1,
    int Y1,
    uint32_t Color
    )
{
    int Dx, Dy, Sx, Sy, Err, E2;

    Dx  = X1 - X0; if (Dx < 0) Dx = -Dx;
    Dy  = Y1 - Y0; if (Dy < 0) Dy = -Dy;
    Sx  = X0 < X1 ? 1 : -1;
    Sy  = Y0 < Y1 ? 1 : -1;
    Err = Dx - Dy;

    for (;;) {
        if (X0 >= 0 && Y0 >= 0) {
            VideoSetPixel((uint32_t)X0, (uint32_t)Y0, Color);
        }
        if (X0 == X1 && Y0 == Y1) break;
        E2 = 2 * Err;
        if (E2 > -Dy) { Err -= Dy; X0 += Sx; }
        if (E2 <  Dx) { Err += Dx; Y0 += Sy; }
    }
}

/* -----------------------------------------------------------------------
   Glyph rendering
   -------------------------------------------------------------------- */

VOID
VideoDrawChar (
    uint32_t X,
    uint32_t Y,
    char     C,
    uint32_t Fg,
    uint32_t Bg
    )
{
    const uint8_t *Glyph;
    uint32_t       Row, Col;
    uint8_t        Bits;

    if ((uint8_t)C < FONT_FIRST || (uint8_t)C > 0x7E) {
        C = '?';
    }

    Glyph = g_Font[(uint8_t)C - FONT_FIRST];

    for (Row = 0; Row < FONT_H; Row++) {
        Bits = Glyph[Row];
        for (Col = 0; Col < FONT_W; Col++) {
            if (Bits & (0x80 >> Col)) {
                VideoSetPixel(X + Col, Y + Row, Fg);
            } else {
                VideoSetPixel(X + Col, Y + Row, Bg);
            }
        }
    }
}

VOID
VideoDrawString (
    uint32_t    X,
    uint32_t    Y,
    const char *S,
    uint32_t    Fg,
    uint32_t    Bg
    )
{
    uint32_t Cx = X;

    for (; *S; S++) {
        if (*S == '\n') {
            Cx  = X;
            Y  += FONT_H;
        } else {
            VideoDrawChar(Cx, Y, *S, Fg, Bg);
            Cx += FONT_W;
        }
    }
}

/* -----------------------------------------------------------------------
   Scrolling console  (used by PrintChar / Print / ClearScreen)
   -------------------------------------------------------------------- */

static VOID
ConScrollUp (
    VOID
    )
{
    uint32_t R, RowPx, B;
    uint32_t Rows      = CON_ROWS;
    uint32_t LineBytes = g_ScreenWidth * g_Bpp;

    for (R = 1; R < Rows; R++) {
        for (RowPx = 0; RowPx < FONT_H; RowPx++) {
            uint8_t *Src = g_FB + (R       * FONT_H + RowPx) * g_Pitch;
            uint8_t *Dst = g_FB + ((R - 1) * FONT_H + RowPx) * g_Pitch;
            for (B = 0; B < LineBytes; B++) {
                Dst[B] = Src[B];
            }
        }
    }

    VideoFillRect(0, (Rows - 1) * FONT_H, g_ScreenWidth, FONT_H, COLOR_BLACK);
}

/* VGA text fallback helpers */
static VOID
VgaScroll (
    VOID
    )
{
    int i;

    for (i = 0; i < (VGA_HEIGHT - 1) * VGA_WIDTH * 2; i++) {
        g_VgaBuf[i] = g_VgaBuf[i + VGA_WIDTH * 2];
    }
    for (i = (VGA_HEIGHT - 1) * VGA_WIDTH * 2; i < VGA_HEIGHT * VGA_WIDTH * 2; i += 2) {
        g_VgaBuf[i]     = ' ';
        g_VgaBuf[i + 1] = 0x07;
    }
    g_VgaCursor -= VGA_WIDTH * 2;
}

static VOID
VgaUpdateCursor (
    VOID
    )
{
    uint16_t Pos = (uint16_t)(g_VgaCursor / 2);
    outb(0x3D4, 0x0F); outb(0x3D5, (uint8_t)(Pos & 0xFF));
    outb(0x3D4, 0x0E); outb(0x3D5, (uint8_t)((Pos >> 8) & 0xFF));
}

/* -----------------------------------------------------------------------
   Public console API
   -------------------------------------------------------------------- */

VOID
PrintChar (
    char Character,
    char Attribute
    )
{
    uint32_t Fg, Bg;

    if (g_TextMode) {
        /* VGA text fallback */
        if (Character == '\n') {
            g_VgaCursor = (g_VgaCursor / (VGA_WIDTH * 2) + 1) * (VGA_WIDTH * 2);
        } else {
            g_VgaBuf[g_VgaCursor++] = Character;
            g_VgaBuf[g_VgaCursor++] = Attribute;
        }
        if (g_VgaCursor >= VGA_SIZE) VgaScroll();
        VgaUpdateCursor();
        return;
    }

    Fg = g_AttrToRgb[(uint8_t)Attribute & 0x0F];
    Bg = g_AttrToRgb[((uint8_t)Attribute >> 4) & 0x0F];

    if (Character == '\n') {
        g_ConCol = 0;
        g_ConRow++;
    } else if (Character == '\r') {
        g_ConCol = 0;
    } else if (Character == '\b') {
        if (g_ConCol > 0) {
            g_ConCol--;
            VideoDrawChar(g_ConCol * FONT_W, g_ConRow * FONT_H, ' ', Fg, COLOR_BLACK);
        }
    } else {
        VideoDrawChar(g_ConCol * FONT_W, g_ConRow * FONT_H, Character, Fg, COLOR_BLACK);
        g_ConCol++;
        if (g_ConCol >= CON_COLS) {
            g_ConCol = 0;
            g_ConRow++;
        }
    }

    if (g_ConRow >= CON_ROWS) {
        ConScrollUp();
        g_ConRow = CON_ROWS - 1;
    }
}

VOID
Print (
    const char *String
    )
{
    int Index;

    for (Index = 0; String[Index]; Index++) {
        PrintChar(String[Index], 0x07);
    }
}

VOID
PrintInt (
    int Number
    )
{
    char Buffer[12];
    int  Index = 0;

    if (Number == 0) { PrintChar('0', 0x07); return; }
    if (Number < 0)  { PrintChar('-', 0x07); Number = -Number; }

    while (Number > 0) {
        Buffer[Index++] = (char)((Number % 10) + '0');
        Number /= 10;
    }
    while (--Index >= 0) {
        PrintChar(Buffer[Index], 0x07);
    }
}

VOID
ClearScreen (
    VOID
    )
{
    int Index;

    if (g_TextMode) {
        for (Index = 0; Index < VGA_SIZE; Index += 2) {
            g_VgaBuf[Index]     = ' ';
            g_VgaBuf[Index + 1] = 0x07;
        }
        g_VgaCursor = 0;
        VgaUpdateCursor();
        return;
    }

    VideoFillRect(0, 0, g_ScreenWidth, g_ScreenHeight, COLOR_BLACK);
    g_ConCol = 0;
    g_ConRow = 0;
}

/* -----------------------------------------------------------------------
   Grid-cell helpers  (used by snake and other direct-draw code)
   -------------------------------------------------------------------- */

/*
 * VideoSetCell
 *   Draws character C with Attribute at grid position (Col, Row) without
 *   disturbing the scrolling console cursor.
 */
VOID
VideoSetCell (
    uint32_t Col,
    uint32_t Row,
    char     C,
    char     Attribute
    )
{
    uint32_t Fg, Bg;

    if (g_TextMode) {
        int Offset = (int)((Row * VGA_WIDTH + Col) * 2);
        g_VgaBuf[Offset]     = C;
        g_VgaBuf[Offset + 1] = Attribute;
        return;
    }

    Fg = g_AttrToRgb[(uint8_t)Attribute & 0x0F];
    Bg = COLOR_BLACK;
    VideoDrawChar(Col * FONT_W, Row * FONT_H, C, Fg, Bg);
}

/*
 * VideoSetConsoleCursor
 *   Moves the logical console cursor to (Col, Row) so the next
 *   PrintChar call lands at that grid position.
 */
VOID
VideoSetConsoleCursor (
    uint32_t Col,
    uint32_t Row
    )
{
    if (g_TextMode) {
        g_VgaCursor = (int)((Row * VGA_WIDTH + Col) * 2);
        VgaUpdateCursor();
        return;
    }

    g_ConCol = Col;
    g_ConRow = Row;
}